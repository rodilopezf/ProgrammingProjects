public class Person{

	 String name;
	 double amount;
	 double spent;
	
	
	public Person(){   
	   name="dave";
	   amount=500;
	   spent=200;
	}   
	   
	   
	   public String getName(){return name;}
       public void setName(String g){name=g;}
       
       public double getAmount(){return amount;}
       public void setAmount(float q){amount=q;}
       
       public double getSpent(){return spent;}
       public void setSpent(float w){spent=w;}
       

       
}       
       